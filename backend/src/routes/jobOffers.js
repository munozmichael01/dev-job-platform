router.get('/job-offers', addUserToRequest, requireAuth, async (req, res) => {
  try {
    const {
      q: search,
      status,
      location,
      sector,
      company,
      externalId,
      sortBy = 'CreatedAt',
      sortOrder = 'DESC',
      limit = 20,
      page = 1
    } = req.query;

    const validatedLimit = Math.min(Math.max(parseInt(limit) || 20, 1), 100);
    const validatedPage = Math.max(parseInt(page) || 1, 1);
    const offset = (validatedPage - 1) * validatedLimit;
    const filters = {
      search: search?.trim() || null,
      status: normalizeStatus(status),
      locations: normalizeLocationFilter(location),
      sector: sector && sector !== 'all' ? String(sector).trim() : null,
      company: company && company !== 'all' ? String(company).trim() : null,
      externalId: externalId && externalId !== 'all' ? String(externalId).trim() : null
    };

    let countQ = applyOfferFilters(supabase.from('JobOffers').select('Id', { count: 'exact', head: true }), req, filters);
    const { count, error: countError } = await countQ;
    if (countError) throw countError;
    const total = count || 0;

    if (total === 0) {
      return res.json(emptyState(validatedPage, validatedLimit, {
        search: filters.search,
        status: filters.status || 'all',
        location: filters.locations.length > 0 ? filters.locations : 'all',
        sector: filters.sector || 'all',
        externalId: filters.externalId || 'all'
      }));
    }

    const allowedSortFields = { CreatedAt: 'CreatedAt', Title: 'Title', CompanyName: 'CompanyName', Status: 'Status', StatusId: 'Status', PublicationDate: 'PublicationDate' };
    const validSortBy = allowedSortFields[sortBy] || 'CreatedAt';
    const ascending = String(sortOrder).toUpperCase() === 'ASC';
    const selectFields = 'Id, ExternalId, Title, DescriptionHtml, DescriptionText, CompanyName, Sector, Category, City, Region, SalaryRaw, SalaryMin, SalaryMax, JobType, ContractType, PublicationDate, CreatedAt, Status, Vacancies';

    let dataQ = applyOfferFilters(supabase.from('JobOffers').select(selectFields), req, filters);
    dataQ = dataQ.order(validSortBy, { ascending }).order('Id', { ascending: false }).range(offset, offset + validatedLimit - 1);
    const { data, error: dataError } = await dataQ;
    if (dataError) throw dataError;
    const offers = (data || []).map(mapOffer);

    const statsBase = () => applyUserFilter(supabase.from('JobOffers').select('Id', { count: 'exact', head: true }), req);
    const [statTotal, statActive, statDraft, statPaused] = await Promise.all([
      statsBase(),
      statsBase().eq('Status', 'active'),
      statsBase().eq('Status', 'draft'),
      statsBase().eq('Status', 'paused')
    ]);

    const hasMore = offset + offers.length < total;
    const lastOffer = offers.length > 0 ? offers[offers.length - 1] : null;
    const nextCursor = hasMore && lastOffer ? { lastCreatedAt: lastOffer.CreatedAt, lastId: lastOffer.id } : null;

    res.json({
      success: true,
      data: offers,
      items: offers,
      hasMore,
      total,
      next: nextCursor,
      pagination: {
        page: validatedPage,
        limit: validatedLimit,
        total,
        pages: Math.ceil(total / validatedLimit),
        hasMore,
        lastCreatedAt: nextCursor?.lastCreatedAt || null,
        lastId: nextCursor?.lastId || null
      },
      stats: {
        totalOffers: statTotal.count || 0,
        activeOffers: statActive.count || 0,
        pendingOffers: statDraft.count || 0,
        pausedOffers: statPaused.count || 0,
        totalSectors: 0,
        totalCities: 0
      },
      filters: {
        search: filters.search,
        status: filters.status || 'all',
        location: filters.locations.length > 0 ? filters.locations : 'all',
        sector: filters.sector || 'all',
        externalId: filters.externalId || 'all'
      }
    });
  } catch (error) {
    console.error('❌ Error listando ofertas:', error.message);
    res.status(500).json({ success: false, error: 'Error interno del servidor', details: error.message });
  }
});

router.put('/job-offers/:id/status', addUserToRequest, requireAuth, async (req, res) => {
  try {
    const offerId = parseInt(req.params.id);
    const nextStatus = normalizeStatus(req.body.status);
    if (!offerId || !nextStatus) return res.status(400).json({ success: false, error: 'ID o estado inválido' });

    let checkQ = supabase.from('JobOffers').select('Id, Title, Status').eq('Id', offerId);
    checkQ = applyUserFilter(checkQ, req);
    const { data: found, error: checkError } = await checkQ.limit(1);
    if (checkError) throw checkError;
    if (!found || found.length === 0) return res.status(404).json({ success: false, error: 'Oferta no encontrada' });

    const current = found[0];
    const { error } = await supabase.from('JobOffers').update({ Status: nextStatus, UpdatedAt: new Date().toISOString() }).eq('Id', offerId);
    if (error) throw error;

    res.json({
      success: true,
      message: `Oferta "${current.Title}" actualizada correctamente`,
      data: { id: offerId, previousStatus: current.Status, newStatus: nextStatus, newStatusId: statusId(nextStatus), title: current.Title }
    });
  } catch (error) {
    res.status(500).json({ success: false, error: 'Error interno del servidor', details: error.message });
  }
});

