const filterCache = {
  locations: { data: null, timestamp: 0, ttl: 5 * 60 * 1000 },
  sectors: { data: null, timestamp: 0, ttl: 5 * 60 * 1000 },
  companies: { data: null, timestamp: 0, ttl: 5 * 60 * 1000 },
  externalIds: { data: null, timestamp: 0, ttl: 10 * 60 * 1000 }
};

const STATUS_TO_ID = { active: 1, paused: 2, archived: 3, draft: 4, expired: 5 };
const ID_TO_STATUS = { 1: 'active', 2: 'paused', 3: 'archived', 4: 'draft', 5: 'expired' };
const ACTIVE_STATUSES = new Set(['active']);

function getCache(key) {
  const cached = filterCache[key];
  if (cached?.data && Date.now() - cached.timestamp < cached.ttl) return cached.data;
  return null;
}

function setCache(key, data) {
  filterCache[key].data = data;
  filterCache[key].timestamp = Date.now();
}

function hasActiveFilterValues(filters) {
  return Object.values(filters).some(value => Array.isArray(value) ? value.length > 0 : Boolean(value));
}

function normalizeStatus(value) {
  if (!value || value === 'all') return null;
  if (typeof value === 'number') return ID_TO_STATUS[value] || null;
  const raw = String(value).toLowerCase();
  if (ID_TO_STATUS[Number(raw)]) return ID_TO_STATUS[Number(raw)];
  if (STATUS_TO_ID[raw]) return raw;
  if (raw === 'pending') return 'draft';
  return null;
}

function statusId(status) {
  return STATUS_TO_ID[normalizeStatus(status) || 'draft'] || 4;
}

function formatSalary(min, max, raw) {
  if (raw) return raw;
  if (min && max && min > 0 && max > 0) return `${Math.round(min).toLocaleString('es')}-${Math.round(max).toLocaleString('es')}€`;
  if (min && min > 0) return `Desde ${Math.round(min).toLocaleString('es')}€`;
  if (max && max > 0) return `Hasta ${Math.round(max).toLocaleString('es')}€`;
  return 'No especificado';
}

function formatLocation(city, region) {
  if (city && region && region.trim()) return `${city}, ${region}`;
  return city || region || null;
}

function sanitizePostgrestOrTerm(value) {
  return String(value || '')
    .trim()
    // Commas and parentheses are control characters inside PostgREST `.or(...)`.
    .replace(/[(),]/g, ' ')
    .replace(/\s+/g, ' ');
}

function parseLocationValue(value) {
  const raw = String(value || '').trim();
  if (!raw) return { city: null, region: null, term: null };

  const parts = raw
    .split(',')
    .map(part => part.trim())
    .filter(Boolean);
  const parsedCity = sanitizePostgrestOrTerm(parts[0]) || null;
  const parsedRegion = sanitizePostgrestOrTerm(parts[parts.length - 1]) || null;

  return {
    city: parsedCity,
    region: parts.length > 1 ? parsedRegion : null,
    term: sanitizePostgrestOrTerm(raw)
  };
}

function normalizeLocationFilter(value) {
  if (!value) return [];
  const values = Array.isArray(value) ? value : [value];
  return values
    .flatMap(item => String(item || '').split('|'))
    .map(item => item.trim())
    .filter(item => item && item !== 'all');
}

function normalizeStringFilter(value) {
  if (!value) return [];
  const values = Array.isArray(value) ? value : [value];
  return values
    .flatMap(item => String(item || '').split('|'))
    .map(item => item.trim())
    .filter(item => item && item !== 'all');
}

function applyMultiOrFilter(query, values, fields) {
  const terms = normalizeStringFilter(values)
    .slice(0, 25)
    .map(sanitizePostgrestOrTerm)
    .filter(Boolean);
  if (terms.length === 0) return query;

  const parts = [];
  terms.forEach(term => {
    fields.forEach(field => parts.push(`${field}.ilike.%${term}%`));
  });

  return parts.length > 0 ? query.or(parts.join(',')) : query;
}

function applyLocationFilter(query, location) {
  const locations = normalizeLocationFilter(location);
  if (locations.length === 0) return query;

  const parts = [];
  locations.slice(0, 25).forEach(item => {
    const parsed = parseLocationValue(item);
    if (!parsed.term) return;

    // Values generated by /job-offers/locations use "City, Region".
    // For imported feeds we can receive "City, City, Province"; matching the
    // first part as city and the last part as broad region keeps the filter
    // stable without relying on exact source formatting.
    if (parsed.city && parsed.region) {
      parts.push(`and(City.ilike.%${parsed.city}%,Region.ilike.%${parsed.region}%)`);
      return;
    }

    parts.push(`City.ilike.%${parsed.term}%`);
    parts.push(`Region.ilike.%${parsed.term}%`);
    parts.push(`Country.ilike.%${parsed.term}%`);
    parts.push(`CountryCode.ilike.%${parsed.term}%`);
  });

  return parts.length > 0 ? query.or(parts.join(',')) : query;
}

function applyUserFilter(query, req) {
  return isSuperAdmin(req) ? query : query.eq('UserId', req.userId);
}

function mapOffer(row) {
  const status = normalizeStatus(row.Status) || 'draft';
  return {
    id: row.Id,
    Id: row.Id,
    OfferId: row.Id, // shim: old frontend/components sometimes still use OfferId
    ExternalId: row.ExternalId,
    title: row.Title,
    Title: row.Title,
    description: row.DescriptionText || row.DescriptionHtml,
    Description: row.DescriptionText || row.DescriptionHtml,
    company: row.CompanyName,
    CompanyName: row.CompanyName,
    sector: row.Sector || row.Category,
    Sector: row.Sector || row.Category,
    City: row.City,
    Region: row.Region,
    location: formatLocation(row.City, row.Region),
    SalaryMin: row.SalaryMin,
    SalaryMax: row.SalaryMax,
    SalaryRaw: row.SalaryRaw,
    salary: formatSalary(row.SalaryMin, row.SalaryMax, row.SalaryRaw),
    type: row.JobType || row.ContractType || 'No especificado',
    JobType: row.JobType,
    publishDate: row.PublicationDate,
    PublicationDate: row.PublicationDate,
    CreatedAt: row.CreatedAt,
    Status: status,
    StatusId: statusId(status),
    status,
    campaignCount: 0,
    segmentCount: 0,
    promotion: 'Sin campañas',
    campaigns: null,
    segments: null,
    totalBudget: 0,
    budgetSpent: 0,
    targetApplications: 0,
    applicationsReceived: 0,
    performance: 'Sin datos'
  };
}

function emptyState(page, limit, filters) {
  return {
    success: true,
    data: [],
    items: [],
    hasMore: false,
    total: 0,
    next: null,
    pagination: { page, limit, total: 0, pages: 0, hasMore: false, lastCreatedAt: null, lastId: null },
    stats: { totalOffers: 0, activeOffers: 0, pendingOffers: 0, pausedOffers: 0, totalSectors: 0, totalCities: 0 },
    filters
  };
}

function applyOfferFilters(query, req, filters) {
  query = applyUserFilter(query, req);
  if (filters.status) query = query.eq('Status', filters.status);
  query = applyLocationFilter(query, filters.locations || filters.location);
  query = applyMultiOrFilter(query, filters.sectors || filters.sector, ['Sector', 'Category']);
  query = applyMultiOrFilter(query, filters.companies || filters.company, ['CompanyName']);
  query = applyMultiOrFilter(query, filters.jobTitles || filters.titles, ['Title', 'CompanyName']);
  if (filters.externalId) query = query.ilike('ExternalId', `%${filters.externalId}%`);
  if (filters.search) {
    const term = sanitizePostgrestOrTerm(filters.search);
    query = query.or(`Title.ilike.%${term}%,CompanyName.ilike.%${term}%,Sector.ilike.%${term}%,Category.ilike.%${term}%,City.ilike.%${term}%,Region.ilike.%${term}%,Country.ilike.%${term}%`);
  }
  return query;
}

router.get('/job-offers/locations', addUserToRequest, requireAuth, async (req, res) => {
  try {
    const filters = {
      status: normalizeStatus(req.query.status),
      sectors: normalizeStringFilter(req.query.sector),
      companies: normalizeStringFilter(req.query.company),
      jobTitles: normalizeStringFilter(req.query.jobTitle || req.query.jobTitles),
      externalId: req.query.externalId && req.query.externalId !== 'all' ? String(req.query.externalId).trim() : null,
      search: req.query.q?.trim() || null
    };
    const hasFilters = hasActiveFilterValues(filters);
    if (!hasFilters) {
      const cached = getCache('locations');
      if (cached) return res.json({ success: true, data: cached });
    }
    let query = supabase.from('JobOffers').select('City, Region').or('City.not.is.null,Region.not.is.null');
    query = applyOfferFilters(query, req, filters);
    const { data, error } = await query;
    if (error) throw error;
    const locations = [...new Set((data || []).map(r => formatLocation(r.City, r.Region)).filter(Boolean))].sort();
    if (!hasFilters) setCache('locations', locations);
    res.json({ success: true, data: locations });
  } catch (error) {
    res.status(500).json({ success: false, error: 'Error interno del servidor', details: error.message });
  }
});

