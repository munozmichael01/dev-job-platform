      DescriptionHtml: body.DescriptionHtml || body.Description || body.description || null,
      DescriptionText: body.DescriptionText || null,
      ApplicationUrl: body.ApplicationUrl || body.applicationUrl || null,
      ExternalUrl: body.ExternalUrl || body.url || null,
      CompanyName: body.CompanyName || body.company || null,
      CompanyExternalId: body.CompanyExternalId || body.companyId || null,
      CompanyLogoUrl: body.CompanyLogoUrl || body.companyLogoUrl || null,
      Country: body.Country || body.country || null,
      CountryCode: body.CountryCode || body.countryCode || null,
      Region: body.Region || body.region || null,
      City: body.City || body.city || null,
      Postcode: body.Postcode || body.postcode || null,
      Sector: body.Sector || body.sector || body.Category || body.category || null,
      Category: body.Category || body.category || null,
      Subcategory: body.Subcategory || body.subcategory || null,
      JobType: body.JobType || body.jobType || null,
      SalaryRaw: body.SalaryRaw || body.salary || null,
      SalaryMin: body.SalaryMin || null,
      SalaryMax: body.SalaryMax || null,
      Vacancies: body.Vacancies || body.vacancies || 1,
      PublicationDate: body.PublicationDate ? new Date(body.PublicationDate).toISOString() : null,
      Status: normalizeStatus(body.Status || body.status) || 'active',
      CreatedAt: new Date().toISOString(),
      UpdatedAt: new Date().toISOString()
